#!/bin/bash
# shellcheck disable=SC2086

source $DIR_BIN$DIR_SLASH"LOG.sh"

log_enter $DIR_BNDL$DIR_SLASH$FILE_INIT

ENV=$DIR_SLASH"env"$DIR_SLASH
FILE_ENV_INT=$DIR_BNDL$ENV$FILE_INIT
log_var FILE_ENV_INT $FILE_ENV_INT
source $FILE_ENV_INT

GIT=$DIR_SLASH"git"$DIR_SLASH
FILE_GIT_INT=$DIR_BNDL$GIT$FILE_INIT
log_var FILE_GIT_INT $FILE_GIT_INT
source $FILE_GIT_INT

LOG=$DIR_SLASH"log"$DIR_SLASH
FILE_LOG_INT=$DIR_BNDL$LOG$FILE_INIT
log_var FILE_LOG_INT $FILE_LOG_INT
source $FILE_LOG_INT

MISC=$DIR_SLASH"misc"$DIR_SLASH
FILE_MISC_INT=$DIR_BNDL$MISC$FILE_INIT
log_var FILE_MISC_INT $FILE_MISC_INT
source $FILE_MISC_INT


log_exit $DIR_BNDL$DIR_SLASH$FILE_INIT

